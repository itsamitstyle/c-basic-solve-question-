//PERIMERTER OF CUBIDE 

#include<iostream>
using namespace std;
int main(){
    //perimeter of cubide 
    int leanght;
    int breadth;
    int height;
    cout <<"enter the value of leanght ";
    scanf("%d",&leanght);
    cout <<"enter the value of breadth ";
    scanf("%d",&breadth);
    cout <<"enter the value of height ";
    scanf("%d",&height);
    int cubide ;
    cubide = 4*leanght*breadth*height;
    cout <<"the value of cubide is %d\n "<<cubide;

    return 0;
}
